// ─── Pure helpers ─────────────────────────────────────────────────────────────

export function getEra(artist) {
  if (!artist) return null;
  if (ARTIST_DECADE[artist]) return ARTIST_DECADE[artist];
  const al = artist.toLowerCase();
  for (const [k, v] of Object.entries(ARTIST_DECADE)) if (k.toLowerCase() === al) return v;
  return null;
}

export function getGenre(artist) {
  if (!artist) return [];
  if (ARTIST_GENRE[artist]) return ARTIST_GENRE[artist];
  const al = artist.toLowerCase();
  for (const [k, v] of Object.entries(ARTIST_GENRE)) if (k.toLowerCase() === al) return v;
  return [];
}

export function getFirstWord(title) {
  return (title || '').trim().replace(/^(a|an|the)\s+/i, '').split(/[\s\-]/)[0].toLowerCase().replace(/[^a-z0-9]/g, '');
}

export function classifyTags(title, artist) {
  const text = (title + ' ' + artist).toLowerCase();
  const scores = {};
  for (const [theme, kws] of Object.entries(THEME_KW)) {
    let s = 0; for (const kw of kws) if (text.includes(kw)) s++;
    if (s > 0) scores[theme] = s;
  }
  const sorted = Object.entries(scores).sort((a, b) => b[1] - a[1]).slice(0, 3).map(e => e[0]);
  return sorted.length ? sorted : ['love'];
}

export function lookupBPM(title, artist) {
  const k1 = `${title}|${artist}`;
  if (BPM_TABLE[k1]) return BPM_TABLE[k1];
  const tl = title.toLowerCase();
  for (const [k, v] of Object.entries(BPM_TABLE)) if (k.split('|')[0].toLowerCase() === tl) return v;
  return null;
}

function slugify(s) {
  return s.toLowerCase().replace(/['']/g, '').replace(/[^a-z0-9\s-]/g, '').trim().replace(/\s+/g, '-');
}

const LS = {
  get: k => { try { const v = localStorage.getItem(k); return v ? JSON.parse(v) : null; } catch { return null; } },
  set: (k, v) => { try { localStorage.setItem(k, JSON.stringify(v)); } catch {} },
};

export async function fetchBPM(title, artist) {
  const cache = LS.get('bfm_bpm') || {};
  const ck = `${title}|${artist}`;
  if (cache[ck] !== undefined) return cache[ck];
  const local = lookupBPM(title, artist);
  if (local) { cache[ck] = local; LS.set('bfm_bpm', cache); return local; }
  try {
    const url = `https://songbpm.com/@${slugify(artist)}/${slugify(title)}`;
    const r = await fetch(`https://api.allorigins.win/get?url=${encodeURIComponent(url)}`,
      { signal: AbortSignal.timeout(5000) });
    if (r.ok) {
      const html = (await r.json()).contents || '';
      const m = html.match(/"bpm"\s*[:\s]+(\d+)/) || html.match(/(\d{2,3})\s*BPM/i);
      if (m) { const bpm = parseInt(m[1]); if (bpm > 40 && bpm < 300) { cache[ck] = bpm; LS.set('bfm_bpm', cache); return bpm; } }
    }
  } catch { /* ignore */ }
  cache[ck] = null; LS.set('bfm_bpm', cache); return null;
}

function lfmTitleCase(s) {
  return s.split(' ').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
}

async function fetchLastfmTags(title, artist) {
  const apiKey = LS.get('bfm_lfm_key');
  if (!apiKey) return null;
  const cache = LS.get('bfm_lfm') || {};
  const ck = `${title}|${artist}`;
  if (cache[ck] !== undefined) return cache[ck];
  try {
    const url = `https://ws.audioscrobbler.com/2.0/?method=track.getTopTags&artist=${encodeURIComponent(artist)}&track=${encodeURIComponent(title)}&api_key=${encodeURIComponent(apiKey)}&format=json`;
    const r = await fetch(url, { signal: AbortSignal.timeout(6000) });
    if (!r.ok) throw new Error('HTTP ' + r.status);
    const data = await r.json();
    if (data?.error) throw new Error(data.message || 'Last.fm error');
    const topTags = (data?.toptags?.tag || []).slice(0, 20);
    const result = { genre: [], themes: [] };
    for (const tag of topTags) {
      const name = (tag.name || '').toLowerCase().trim();
      if (!name) continue;
      if (LASTFM_GENRES.has(name)) {
        const normalized = lfmTitleCase(name);
        if (!result.genre.includes(normalized)) result.genre.push(normalized);
      }
      if (LASTFM_THEME_MAP[name]) {
        const theme = LASTFM_THEME_MAP[name];
        if (!result.themes.includes(theme)) result.themes.push(theme);
      }
    }
    cache[ck] = result; LS.set('bfm_lfm', cache);
    return result;
  } catch {
    cache[ck] = null; LS.set('bfm_lfm', cache);
    return null;
  }
}

/**
 * Enrich a batch of songs in place.
 * @param {Array} songs
 * @param {Function} onProgress (done, total, status) => void
 * @returns {Promise<Array>} mutated songs
 */
export async function enrichSongs(songs, onProgress) {
  const total = songs.length;

  // Sync enrichment — era, genre, firstWord, tags
  for (const s of songs) {
    if (!s.era)  s.era  = getEra(s.artist);
    if (!s.genre || !s.genre.length) s.genre = getGenre(s.artist);
    s.firstWord = getFirstWord(s.title);
    if (!s.tags || !s.tags.length)   s.tags  = classifyTags(s.title, s.artist);
  }

  // Async BPM enrichment
  const needBpm = songs.filter(s => s.bpm == null);
  let done = 0;
  for (let i = 0; i < needBpm.length; i += 4) {
    await Promise.all(needBpm.slice(i, i + 4).map(async s => {
      s.bpm = await fetchBPM(s.title, s.artist);
      done++;
      onProgress?.(done, needBpm.length, `Enriching ${done}/${needBpm.length}…`);
    }));
  }

  // Last.fm tag enrichment
  const apiKey = LS.get('bfm_lfm_key');
  if (apiKey) {
    done = 0;
    for (let i = 0; i < songs.length; i += 4) {
      await Promise.all(songs.slice(i, i + 4).map(async s => {
        const result = await fetchLastfmTags(s.title, s.artist);
        if (result) {
          for (const g of result.genre) if (!s.genre.includes(g)) s.genre.push(g);
          for (const t of result.themes) if (!s.tags.includes(t)) s.tags.push(t);
        }
        done++;
        onProgress?.(done, songs.length, `Last.fm tags: ${done}/${songs.length}…`);
      }));
    }
  }

  return songs;
}
